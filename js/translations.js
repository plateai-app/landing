// Translation system implementation
class PlateAITranslator {
  constructor() {
    this.currentLanguage = localStorage.getItem('preferred_language') || 'en';
    this.translations = translations;
    this.init();
  }

  init() {
    this.updateLanguageButtons();
    if (this.currentLanguage === 'sw') {
      this.translatePage();
    }
  }

  setLanguage(lang, button) {
    const buttons = document.querySelectorAll('.lang-btn');
    buttons.forEach(btn => btn.classList.remove('active'));
    button.classList.add('active');
    
    this.currentLanguage = lang;
    localStorage.setItem('preferred_language', lang);
    
    if (lang === 'sw') {
      this.translatePage();
    } else {
      this.resetToEnglish();
    }
  }

  translatePage() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
      const key = element.getAttribute('data-translate');
      if (this.translations.sw[key]) {
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
          element.placeholder = this.translations.sw[key];
        } else {
          element.textContent = this.translations.sw[key];
        }
      }
    });

    // Handle special cases that don't use data-translate
    this.translateSpecialElements();
  }

  translateSpecialElements() {
    // Navigation links without data-translate
    const navLinks = {
      'Home': 'Nyumbani',
      'For Nutritionist': 'Kwa Mtaalamu wa Lishe', 
      'About': 'Kuhusu',
      'Contact': 'Mawasiliano'
    };

    document.querySelectorAll('.nav-link').forEach(link => {
      const englishText = link.textContent.trim();
      if (navLinks[englishText]) {
        link.textContent = navLinks[englishText];
      }
    });

    // Update option elements in selects
    const selectOptions = {
      'Choose consultation type': 'Chagua aina ya ushauri',
      'Nutrition Consultation': 'Ushauri wa Lishe',
      'Meal Planning Help': 'Msaada wa Kupanga Mlo',
      'Health Condition Support': 'Msaada wa Hali ya Afya',
      'Product Demo & Questions': 'Onyesho la Bidhaa na Maswali',
      'Partnership Inquiry': 'Hoja ya Ushirikiano',
      'General Information': 'Taarifa za Jumla',
      'Any time is fine': 'Wakati wowote ni mzuri',
      'Morning (9AM - 12PM)': 'Asubuhi (9AM - 12PM)',
      'Afternoon (12PM - 5PM)': 'Mchana (12PM - 5PM)',
      'Evening (5PM - 8PM)': 'Jioni (5PM - 8PM)'
    };

    document.querySelectorAll('option').forEach(option => {
      const englishText = option.textContent.trim();
      if (selectOptions[englishText]) {
        option.textContent = selectOptions[englishText];
      }
    });

    // Update coming soon alert
    const comingSoonElements = document.querySelectorAll('[onclick*="Coming Soon"]');
    comingSoonElements.forEach(element => {
      const onclick = element.getAttribute('onclick');
      if (onclick.includes('lishe.plateai.app')) {
        element.setAttribute('onclick', "alert('Inakuja Hivi Karibuni: lishe.plateai.app')");
      }
    });
  }

  resetToEnglish() {
    // Reload the page to restore original English content
    location.reload();
  }

  updateLanguageButtons() {
    const buttons = document.querySelectorAll('.lang-btn');
    buttons.forEach(btn => {
      btn.classList.remove('active');
      if (btn.textContent.toLowerCase() === this.currentLanguage) {
        btn.classList.add('active');
      }
    });
  }

  // Method to get translation for dynamic content
  translate(key) {
    return this.translations[this.currentLanguage][key] || key;
  }
}

// Initialize translator when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
  window.plateAITranslator = new PlateAITranslator();
});

// Global function for language switching (called from HTML)
function setLanguage(lang, button) {
  if (window.plateAITranslator) {
    window.plateAITranslator.setLanguage(lang, button);
  }
}

// Export for use in other files
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { translations, PlateAITranslator };
}
